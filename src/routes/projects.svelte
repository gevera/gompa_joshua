	<div id="theory">
		<Frame>
			<h3>Theory and methods</h3>
			<p>
				The essence of Morita's method may be summarized in three rules: Accept all your feelings,
				know your purpose(s), and do what needs to be done. When once asked what shy people should
				do, Morita replied, "Sweat."
			</p>
			<ul>
				<li>
					Accept your feelings - Accepting feelings is not ignoring them or avoiding them, but
					welcoming them; Vietnamese poet and writer Thich Nhat Hanh recommends we say, "Hello
					Loneliness, how are you today? Come, sit by me and I will take care of you." Morita's
					advice: "In feelings, it is best to be wealthy and generous" - that is, have many and let
					them fly as they wish.
				</li>
				<li>
					Know your purpose - Implicit in Morita's method, and the traditional Buddhist
					psychological principles which he adapted, is an independence of thought and action,
					something a little alien to the Western ideal to "follow our whims and moods". Morita held
					that we can no more control our thoughts than we can control the weather, as both are
					phenomena of most amazingly complex natural systems. And if we have no hope of controlling
					our emotions, we can hardly be held responsible any more than we can be held responsible
					for feeling hot or cold. We do, however, have complete dominion over our behavior, and for
					Morita, that is a sacred responsibility. "What needs doing now?" is like a mantra in his
					methods.
				</li>
				<li>
					Do what needs doing - One can feel crushed and alone or hurt and homicidal while pulling
					up the weeds in your garden, but one would not be doing it at all if one had not intended
					to raise flowers. Morita's way of treatment is very different from the Western
					diagnosis/disease model. Morita's methods lead his "students" through experiments, and in
					each assignment, the lesson is not explained by a master, but learned first hand, through
					the "doing" or taiken, that knowledge gained by direct experience.
				</li>
			</ul>
			<p>
				Morita therapy is an ecological, purpose-centered, response-oriented therapy created through
				case-based research by Shoma Morita, M.D. (1874–1938). Morita developed his theory of
				consciousness and medically-grounded four-stage progressive therapeutic method with rigor.
				The goal of Morita therapy is to have the patient accept life as it is. This does not mean
				that the patient cannot set and achieve goals, but be able to be satisfied with their life
				in the moment. Morita therapy places an emphasis on letting nature take its course. This is
				not to say that patients should be resigned to their mental illness but to respect the laws
				of nature. Feeling emotions is a law of nature according to Morita therapy. Morita therapy
				helps its patients understand that experiencing emotions, positive or negative, is a facet
				of being a human being. Morita describes his own therapy as follows:
			</p>
			<p class="quote">
				"Although I tried various therapies, including hypnosis for clients with anxiety disorders,
				I did not obtain results beyond the temporary relief of symptoms in clients. I also used the
				life-control method for many years and followed Binswanger's (1911) theory, only to find it
				manneristic, too theoretical, relatively impractical, and ineffective. Binswanger's methods
				deprived my clients of spontaneous activity. Initially, I tried to modify and extend these
				existing systems, but later designed my own method of treatment."
			</p>

			<p>
				Morita therapy was originally developed to address shinkeishitsu, an outdated term used in
				Japan to describe patients who have various types of anxiety. Morita therapy was designed
				not to completely rid the patient of shinkeishitsu but to lessen the damaging effects. While
				Morita therapy has been described as cognate to Albert Ellis's rational-emotive therapy,
				this description does not account for the ecological foundation of the therapy. Morita
				therapy also has commonalities with existential and cognitive behavioral therapy.
			</p>
			<h3>Background</h3>
			<p>
				Shoma Morita, M.D. (1874–1938) was a psychiatrist, researcher, philosopher, and academic
				department chair at Jikei University School of Medicine in Tokyo. Morita's personal training
				in Zen influenced his teachings, yet Morita therapy is not a Zen practice. However, his
				treatment contains paradoxical methods that reflect Zen. Morita formulated his
				psychotherapeutic principles in Japan as a program for the treatment of neurotic tendencies
				at the same time that attention in Europe was given to Sigmund Freud's discovery of the
				unconscious and Carl Jung's development of archetypes.
			</p>
			<h3>Underlying philosophy</h3>
			<p>
				As clients move through the four stages, their senses are activated and curiosity about the
				natural world increases. Herein, they engage and respond more spontaneously and creatively,
				while gaining a sense of their authentic self (Fujita, C., 1986. Morita Therapy: A
				Psychotherapeutic System for Neurosis. Tokyo: Igaku-Shoin). For Morita, it was the
				progressive design and ecological context that made it unique to other therapies of his
				time, as well as today. Therapeutic change runs deeper than thinking and behaving. One's
				perception of self-in-the-world shifts while moving through the therapeutic stages. A
				client's mind, body, and imagination have therapeutic time in a safe place in a natural
				environment to become revitalized; this is particularly necessary when they have survived
				trauma ('Classic Morita Therapy'). For instance, while pulling weeds and watching earthworms
				seek moist soil, they notice that their anxiety has dissipated and they soon embody such
				experiences. It is the therapist's role to observe, facilitate, and reinforce these
				experiences.
			</p>
			<p>
				Morita therapy directs one's attention receptively to what reality brings in each moment—a
				focus on the present, avoiding intellectualising. Simple seeing what is (without judging),
				allows for active responding to what needs doing. Most therapies strive to reduce symptoms.
				Morita therapy, however, aims at building character to enable one to take action
				responsively in life regardless of symptoms, natural fears, and wishes. Character is
				determined by behavior, by what one does. Dogmatic patterns of collapse are replaced with
				the flexibility to call upon courage and empowerment. Decisions become grounded in purpose
				rather than influenced by the fluid flow of feelings.
			</p>
			<p>
				In Morita therapy, character is developed by cultivating awareness, decentralizing the self,
				and honoring the rhythms of the natural environment and one's own diurnal rhythms. Aspects
				of mindfulness are contained in knowing what is controllable and what is not controllable,
				and seeing what is so without attachment to expectations. Knowing what one is doing, knowing
				what the situation is requiring, and knowing the relationship between the two are
				quintessential to self-validation, effective living, and personal fulfillment. Character is
				developed as one moves from being feeling-centered to being purpose-centered. A
				feeling-centered person attends to feelings to such an extent that the concern for
				self-protection reigns over decisions and perceptions. Given the human condition, change,
				pain, and pleasure are natural experiences. Indeed, emotions are a rich type of experience
				and a valuable source of information. Feelings are acknowledged even when what is to be done
				requires not acting on them. Constructive action is no longer put on hold in order to
				process or cope with symptoms or feelings. The individual can focus on the full scope of the
				present moment as the guide for determining what needs to be done.
			</p>
			<p>
				Ultimately, the successful student of Morita therapy learns to accept the internal
				fluctuations of thoughts and feelings and ground his or her behavior in reality and the
				purpose of the moment. Cure is not defined by the alleviation of discomfort or the
				attainment of some ideal feeling state (which the philosophy of this approach opposes), but
				by taking constructive action in one's life which helps one to live a full and meaningful
				existence and not be ruled by one's emotional state.
			</p>
